				<p><h5>Background</h5></p>
				<p>The world has been rapidly changing, and markets that were previously inaccessible are opening up and now offer great opportunities for firms to expand and develop.&nbsp; This increase in global business has also increased the pressure on global logistics channels and infrastructure. In addition, we have seen a radical shift in power from sellers to buyers in business relationships as customers now have easy access to information about the product, price, quality, manufacturing and even to distribution cost. As a result, customers now have more access to product and service choices than ever before in the global market. &nbsp;In this global business environment, the creation of <em>customer value</em> is a key driver of competitive advantage.&nbsp; At the enterprise level, the design of the value chain is intimately linked with the management of a supply chain and its logistics operation.&nbsp; In essence, logistics and supply chain management represents a unified cross-functional process by which good and services would be produced for customers and delivered for their consumption.&nbsp; As a result, logistics and supply chain management is now developed into a core component of the corporate strategy and profitability.</p>

				<p>There are reasons for the poor performance of the local economy and business and global trade in Central Asia. In additional to being landlocked, the distance between Central Asia and major global markets and the high cost of transportation limit or even prevent trade. The poor logistics and transportation infrastructure of this mountain region has been a limiting factor to the regional economic and business development activities.&nbsp; The current realities are high costs and long delays in moving goods over long distances in Central Asia, and these impediments to trade cut off the most important avenue for their expansion and discourage firms from developing the expertise and scale of production that would allow them to compete in the Central Asia region or beyond. In addition, the lack of trained and experienced logistics specialists is further degrading the current regional economic and business growth and not fully benefiting the future opportunities in the global market.&nbsp;</p>

				<p>The improved transport connectivity and border practices can mean faster and cheaper transportation facilitating and increasing trade within the region and beyond. Good transport and logistics services managed by the skilled logistics specialists can enhance competitiveness and trade in every sector of economy. An upgrade service provided by the transport logistics industry will enhance all the economic activities in producing and moving goods across multiple borders.</p>

				<p>Given these global logistics trends and modern challenges of our region, the main objectives of this Train-the-Trainer (TTT) is to train and equip local instructors and professionals working in the logistics related fields with the latest and advanced knowledge, skills, and tools to train regional university students and prepare them for global logistics market and economic activities. We believe that this TTT will serve as a catalyst in quickly building up the local logistics capacity and a sizable labor force that is urgently needed in CA at this point and thus energize regional economic and business activities.&nbsp; &nbsp;</p>

