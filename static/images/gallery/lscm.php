						<h5>Today’s Enterprise Environment<sup>1</sup></h5>
						<img src="../tessa/images/1.jpg" class="img_f" />
						<p>In today’s business environment, market and economics forces are driving the trend toward globalization and at the same time an enterprise is facing increased competitions, smarter and more demanding customers, 
						less brand loyalties, and shorter product life cycles and longer supply chains. In this globally competitive business environment, the creation of customer value is a key driver of competitive advantage, 
						and this customer value should be measured in price, quality, and time in providing products and services to domestic and international customers. At the enterprise level, 
						the design of value chain is intimately linked with the management of operations and supply chains. Operations, as core activities of most business organizations, are the sequence of value-adding processes that manufacture goods or provide services. 
						Operations activities, including production, are directly impacted by the effectiveness of logistics and supply chain management, particularly and significantly by procurement, transportation, distribution, and inventory management functions.</p>
						
						<h5>CA Region Integration<sup>2</sup></h5>
						<img src="../tessa/images/2.jpg" class="img_f" />
						<p>There are many reasons for the poor performance of economy and global trade in Central Asia. In additional to being landlocked, the distance between Central Asia and major global markets and the high cost of transportation limit or even prevent trade. 
						The current realities are high costs and long delays moving goods over long distances, and these conditions do not encourage many enterprises to keep pace with global developments in their industry to become globally competitive. 
						These impediments to trade cut off the most important avenue for their expansion and discourage firms from developing the expertise and scale of production that would allow them to compete in the region or beyond.</p>
						
						<h5>Transport and Logistics Sector<sup>3</sup></h5>
						<img src="../tessa/images/3.jpg" class="img_f" />
						<p>The improved transport connectivity and border practices mean faster, cheaper transportation facilitating and increasing trade within the region and beyond. Good transport and logistics services enhance competitiveness and trade in every sector of economy and are the heart of the global supply chain. 
						The transport logistics industry includes all the services that are needed to move goods across borders to final destinations. Good logistics services are essential for perishable goods and just-in-time shipments that cross multiple borders and intermodal transfers.</p>
						
